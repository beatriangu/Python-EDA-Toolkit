def _print_section(title: str) -> None:
    print(f"\n{title}")
    print("=" * 60)


def _print_items(items: Any) -> None:
    if not items:
        print("None detected.")
        return

    for item in items:
        print(f"- {item}")


def _get_dataset_name(data: Any) -> str:
    if isinstance(data, (str, Path)):
        return Path(data).name
    return "DataFrame"


def _round(value: Any, decimals: int = 4) -> Any:
    try:
        if pd.isna(value):
            return None
        return round(float(value), decimals)
    except (TypeError, ValueError):
        return value


def _format_mb(value: Any) -> str:
    try:
        return f"{float(value):.2f} MB"
    except (TypeError, ValueError):
        return "N/A"


