def _calculate_outlier_summary(
    df: pd.DataFrame,
    numeric_columns: list[str] | None = None,
) -> list[dict[str, Any]]:
    work_df = _sample_df(df, OUTLIER_SAMPLE_ROWS)
    numeric_columns = numeric_columns or work_df.select_dtypes(
        include=["number"],
    ).columns.tolist()

    summary: list[dict[str, Any]] = []

    for column in numeric_columns:
        if column not in work_df.columns:
            continue

        series = work_df[column].dropna()

        if series.empty or series.nunique(dropna=True) <= 1:
            continue

        q1 = series.quantile(0.25)
        q3 = series.quantile(0.75)
        iqr = q3 - q1

        if iqr == 0 or pd.isna(iqr):
            continue

        lower = q1 - 1.5 * iqr
        upper = q3 + 1.5 * iqr
        count = int(((series < lower) | (series > upper)).sum())

        if count > 0:
            estimated_count = int(round(count * (len(df) / len(work_df))))
            summary.append(
                {
                    "column": column,
                    "outliers": estimated_count,
                    "outlier_ratio": _round(count / max(len(series), 1), 4),
                }
            )

    return sorted(summary, key=lambda item: item["outliers"], reverse=True)


def _calculate_skewness_summary(
    df: pd.DataFrame,
    numeric_columns: list[str] | None = None,
    threshold: float = 1.0,
) -> list[dict[str, Any]]:
    work_df = _sample_df(df, OUTLIER_SAMPLE_ROWS)
    numeric_columns = numeric_columns or work_df.select_dtypes(
        include=["number"],
    ).columns.tolist()

    summary: list[dict[str, Any]] = []

    for column in numeric_columns:
        if column not in work_df.columns:
            continue

        series = work_df[column].dropna()

        if series.empty or series.nunique(dropna=True) <= 1:
            continue

        skewness = series.skew()

        if pd.notna(skewness) and abs(skewness) >= threshold:
            summary.append(
                {
                    "column": column,
                    "skewness": _round(skewness, 4),
                }
            )

    return sorted(
        summary,
        key=lambda item: abs(item["skewness"]),
        reverse=True,
    )


def _calculate_target_correlations(
    df: pd.DataFrame,
    target: str | None,
    numeric_columns: list[str] | None = None,
    top_n: int = 8,
) -> list[dict[str, Any]]:
    if not target or target not in df.columns:
        return []

    if not pd.api.types.is_numeric_dtype(df[target]):
        return []

    work_df = _sample_df(df, TARGET_CORR_SAMPLE_ROWS)
    numeric_columns = numeric_columns or work_df.select_dtypes(
        include=["number"],
    ).columns.tolist()

    candidate_columns = [column for column in numeric_columns if column != target]
    correlations: list[dict[str, Any]] = []

    for column in candidate_columns:
        if column not in work_df.columns:
            continue

        data = work_df[[column, target]].dropna()

        if data.empty or data[column].nunique() <= 1 or data[target].nunique() <= 1:
            continue

        corr = data[column].corr(data[target])

        if pd.notna(corr):
            correlations.append(
                {
                    "feature": column,
                    "correlation": _round(corr, 4),
                    "abs_correlation": abs(corr),
                }
            )

    return sorted(
        correlations,
        key=lambda item: item["abs_correlation"],
        reverse=True,
    )[:top_n]


def _build_data_profile(
    df: pd.DataFrame,
    target: str | None = None,
    column_types: dict[str, list[str]] | None = None,
    problem_type: str | None = None,
) -> dict[str, Any]:
    column_types = column_types or _detect_column_types(df)

    missing_by_column = df.isnull().sum()
    missing_by_column = missing_by_column[missing_by_column > 0].sort_values(
        ascending=False,
    )

    numeric_columns = column_types["numeric"]
    total_missing = int(missing_by_column.sum())
    duplicate_rows = int(df.duplicated().sum())

    return {
        "rows": int(df.shape[0]),
        "columns": int(df.shape[1]),
        "total_missing": total_missing,
        "missing_ratio": _round(
            total_missing / max(df.shape[0] * df.shape[1], 1),
            4,
        ),
        "duplicate_rows": duplicate_rows,
        "duplicate_ratio": _round(
            duplicate_rows / max(len(df), 1),
            4,
        ),
        "memory_mb": _round(
            df.memory_usage(deep=True).sum() / 1024**2,
            2,
        ),
        "problem_type": problem_type,
        "target": target,
        "column_types": column_types,
        "missing_by_column": [
            {
                "column": column,
                "missing": int(count),
                "missing_ratio": _round(count / max(len(df), 1), 4),
            }
            for column, count in missing_by_column.items()
        ],
        "outliers": _calculate_outlier_summary(df, numeric_columns=numeric_columns),
        "skewness": _calculate_skewness_summary(df, numeric_columns=numeric_columns),
        "target_correlations": _calculate_target_correlations(
            df,
            target,
            numeric_columns=numeric_columns,
        ),
    }


def _generate_dataset_warnings(
    df: pd.DataFrame,
    target: str | None = None,
    column_types: dict[str, list[str]] | None = None,
    problem_type: str | None = None,
    data_profile: dict[str, Any] | None = None,
) -> list[dict[str, str]]:
    column_types = column_types or _detect_column_types(df)
    data_profile = data_profile or _build_data_profile(
        df,
        target,
        column_types,
        problem_type,
    )

    warnings_list: list[dict[str, str]] = []

    duplicate_count = data_profile["duplicate_rows"]
    duplicate_ratio = data_profile["duplicate_ratio"] or 0
    total_missing = data_profile["total_missing"]
    missing_ratio = data_profile["missing_ratio"] or 0

    if duplicate_count > 0:
        warnings_list.append(
            {
                "level": "danger" if duplicate_ratio >= 0.05 else "warning",
                "title": "Duplicate rows",
                "message": (
                    f"{duplicate_count} duplicate rows detected "
                    f"({duplicate_ratio:.2%})."
                ),
            }
        )

    if total_missing > 0:
        warnings_list.append(
            {
                "level": "danger" if missing_ratio >= 0.05 else "warning",
                "title": "Missing values",
                "message": f"{total_missing} missing cells detected ({missing_ratio:.2%}).",
            }
        )

    if target and target in df.columns:
        target_missing = int(df[target].isnull().sum())

        if target_missing > 0:
            warnings_list.append(
                {
                    "level": "danger",
                    "title": "Missing target values",
                    "message": (
                        f"Target column '{target}' contains "
                        f"{target_missing} missing values."
                    ),
                }
            )

        if problem_type == "classification":
            class_ratio = df[target].value_counts(normalize=True, dropna=True)

            if not class_ratio.empty and class_ratio.max() > 0.70:
                warnings_list.append(
                    {
                        "level": "warning",
                        "title": "Potential class imbalance",
                        "message": (
                            f"The largest class represents "
                            f"{class_ratio.max():.2%} of known target values."
                        ),
                    }
                )

    if column_types["constant"]:
        warnings_list.append(
            {
                "level": "warning",
                "title": "Constant columns",
                "message": (
                    "Remove columns with a single value: "
                    f"{column_types['constant'][:8]}."
                ),
            }
        )

    if column_types["id"]:
        warnings_list.append(
            {
                "level": "warning",
                "title": "Potential ID columns",
                "message": (
                    "Exclude ID-like columns from model features: "
                    f"{column_types['id'][:8]}."
                ),
            }
        )

    if column_types["high_cardinality"]:
        warnings_list.append(
            {
                "level": "warning",
                "title": "High-cardinality categoricals",
                "message": (
                    "Avoid naive one-hot encoding for: "
                    f"{column_types['high_cardinality'][:8]}."
                ),
            }
        )

    if column_types["datetime"]:
        warnings_list.append(
            {
                "level": "warning",
                "title": "Datetime columns",
                "message": (
                    "Consider temporal features and chronological validation "
                    "if the task is time-based."
                ),
            }
        )

    if column_types["text"]:
        warnings_list.append(
            {
                "level": "warning",
                "title": "Text columns",
                "message": (
                    "Text-like columns may require NLP preprocessing: "
                    f"{column_types['text'][:8]}."
                ),
            }
        )

    if data_profile["outliers"]:
        top = data_profile["outliers"][0]
        warnings_list.append(
            {
                "level": "warning",
                "title": "Numerical outliers",
                "message": (
                    f"'{top['column']}' contains approximately "
                    f"{top['outliers']} IQR-based outliers."
                ),
            }
        )

    return warnings_list or [
        {
            "level": "success",
            "title": "No important warnings",
            "message": "No major structural quality risks were detected.",
        }
    ]


def _generate_smart_insights(
    df: pd.DataFrame,
    target: str | None = None,
    column_types: dict[str, list[str]] | None = None,
    problem_type: str | None = None,
    data_profile: dict[str, Any] | None = None,
) -> list[str]:
    column_types = column_types or _detect_column_types(df)
    data_profile = data_profile or _build_data_profile(
        df,
        target,
        column_types,
        problem_type,
    )

    insights: list[str] = []

    if data_profile["missing_by_column"]:
        top = data_profile["missing_by_column"][0]
        insights.append(
            f"Column '{top['column']}' has the highest missingness: "
            f"{top['missing']} values ({top['missing_ratio']:.2%})."
        )

    if data_profile["target_correlations"]:
        top = data_profile["target_correlations"][0]
        direction = "positive" if top["correlation"] >= 0 else "negative"
        insights.append(
            f"'{top['feature']}' has the strongest linear relationship "
            f"with the target ({direction}: {top['correlation']})."
        )

    if data_profile["skewness"]:
        top = data_profile["skewness"][0]
        insights.append(
            f"'{top['column']}' is highly skewed "
            f"(skewness: {top['skewness']})."
        )

    if data_profile["outliers"]:
        top = data_profile["outliers"][0]
        insights.append(
            f"'{top['column']}' shows the highest number of IQR-based "
            f"outliers ({top['outliers']})."
        )

    if problem_type:
        insights.append(f"Detected analytical setup: {problem_type}.")

    if column_types["numeric"] and column_types["categorical"]:
        insights.append(
            "The dataset contains numerical and categorical features, "
            "so preprocessing should be model-specific."
        )

    return list(dict.fromkeys(insights))[:8]


def _generate_next_steps(
    df: pd.DataFrame,
    target: str | None = None,
    problem_type: str | None = None,
    column_types: dict[str, list[str]] | None = None,
    data_profile: dict[str, Any] | None = None,
    preprocessing_suggestions: list[str] | None = None,
    model_suggestions: list[str] | None = None,
) -> list[str]:
    column_types = column_types or _detect_column_types(df)
    data_profile = data_profile or _build_data_profile(
        df,
        target,
        column_types,
        problem_type,
    )

    steps: list[str] = []

    if data_profile["total_missing"] > 0:
        steps.append(
            "Review missing values and choose imputation or row removal before training."
        )

    if data_profile["duplicate_rows"] > 0:
        steps.append(
            "Confirm whether duplicate rows are valid observations or should be removed."
        )

    if column_types["constant"]:
        steps.append(
            "Remove constant columns because they do not add predictive information."
        )

    if column_types["id"]:
        steps.append("Exclude ID-like columns from standard model features.")

    if column_types["high_cardinality"]:
        steps.append(
            "Use rare-category grouping, frequency encoding or target encoding "
            "for high-cardinality categoricals."
        )
    elif column_types["categorical"]:
        steps.append("Encode low-cardinality categorical variables with one-hot encoding.")

    if column_types["numeric"]:
        steps.append(
            "Scale numerical features for linear, distance-based or gradient-based models."
        )

    if data_profile["outliers"]:
        steps.append(
            "Inspect numerical outliers and decide whether to keep, cap or transform them."
        )

    if problem_type in {"classification", "regression"} and target:
        steps.append(
            "Create a train/test split and keep the test set untouched for final evaluation."
        )

    if model_suggestions:
        steps.append(
            "Start with the simplest baseline, then compare stronger candidate models."
        )

    return list(dict.fromkeys(steps))[:8]


def _build_report_payload(
    df: pd.DataFrame,
    dataset_name: str,
    target: str | None,
    problem_type: str,
    column_types: dict[str, list[str]],
    data_profile: dict[str, Any],
    preprocessing_suggestions: list[str],
    model_suggestions: list[str],
) -> dict[str, Any]:
    return {
        "dataset_name": dataset_name,
        "dataset_shape": df.shape,
        "missing_values": data_profile["total_missing"],
        "preprocessing_suggestions": preprocessing_suggestions,
        "model_suggestions": model_suggestions,
        "problem_type": problem_type,
        "target": target,
        "column_types": column_types,
        "data_profile": data_profile,
        "dataset_warnings": _generate_dataset_warnings(
            df,
            target,
            column_types,
            problem_type,
            data_profile,
        ),
        "smart_insights": _generate_smart_insights(
            df,
            target,
            column_types,
            problem_type,
            data_profile,
        ),
        "next_steps": _generate_next_steps(
            df,
            target,
            problem_type,
            column_types,
            data_profile,
            preprocessing_suggestions,
            model_suggestions,
        ),
    }


def _call_generate_html_report(
    report_payload: dict[str, Any],
    report_path: str,
) -> Any:
    report_payload = dict(report_payload)
    report_payload["output_path"] = report_path

    try:
        signature = inspect.signature(generate_html_report)
        accepted = set(signature.parameters)
        filtered_payload = {
            key: value
            for key, value in report_payload.items()
            if key in accepted
        }
        return generate_html_report(**filtered_payload)

    except (TypeError, ValueError):
        fallback_payload = {
            "dataset_name": report_payload["dataset_name"],
            "dataset_shape": report_payload["dataset_shape"],
            "missing_values": report_payload["missing_values"],
            "preprocessing_suggestions": report_payload["preprocessing_suggestions"],
            "model_suggestions": report_payload["model_suggestions"],
            "output_path": report_path,
        }
        return generate_html_report(**fallback_payload)


