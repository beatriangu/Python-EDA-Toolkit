def _detect_problem_type(
    df: pd.DataFrame,
    target: str | None = None,
    date_column: str | None = None,
    user_column: str | None = None,
    item_column: str | None = None,
    task_type: str = "auto",
) -> str:
    valid_task_types = {
        "auto",
        "exploratory",
        "classification",
        "regression",
        "time_series",
        "recommendation",
        "nlp",
    }

    if task_type not in valid_task_types:
        raise ValueError(
            f"Invalid task_type '{task_type}'. "
            f"Valid values are: {sorted(valid_task_types)}"
        )

    if task_type != "auto":
        return task_type

    if user_column and item_column:
        return "recommendation"

    if date_column:
        return "time_series"

    if target is None:
        sampled_df = _sample_df(df, PROFILE_SAMPLE_ROWS)
        return "nlp" if _detect_text_columns(sampled_df) else "exploratory"

    y = df[target].dropna()

    if y.empty:
        return "exploratory"

    unique_values = y.nunique(dropna=True)
    unique_ratio = unique_values / max(len(y), 1)

    if not pd.api.types.is_numeric_dtype(y):
        return "classification"

    if unique_values <= 20 or unique_ratio <= 0.05:
        return "classification"

    return "regression"


