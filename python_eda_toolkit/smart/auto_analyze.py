def _sample_df(
    df: pd.DataFrame,
    max_rows: int,
    random_state: int = 42,
) -> pd.DataFrame:
    if len(df) <= max_rows:
        return df

    return df.sample(
        n=max_rows,
        random_state=random_state,
    )


def _clean_dataframe_columns(df: pd.DataFrame) -> tuple[pd.DataFrame, list[str]]:
    """Remove autogenerated index columns and duplicated column names.

    This keeps demos and reports clean when CSV files were saved with an
    index, creating columns such as `Unnamed: 0` or `Unnamed: 0.1`.
    """

    df = df.copy()

    removed_columns: list[str] = []

    unnamed_columns = [
        column
        for column in df.columns
        if str(column).startswith("Unnamed")
    ]

    if unnamed_columns:
        df = df.drop(columns=unnamed_columns)
        removed_columns.extend([str(column) for column in unnamed_columns])

    duplicated_mask = df.columns.duplicated()

    if duplicated_mask.any():
        duplicated_columns = df.columns[duplicated_mask].tolist()
        df = df.loc[:, ~duplicated_mask]
        removed_columns.extend([str(column) for column in duplicated_columns])

    return df, removed_columns


def _optimize_dataframe_memory(df: pd.DataFrame) -> pd.DataFrame:
    """Downcast numeric/bool columns without changing public behavior."""

    df = df.copy()

    for column in df.select_dtypes(include=["float64"]).columns:
        df[column] = pd.to_numeric(df[column], downcast="float")

    for column in df.select_dtypes(include=["int64"]).columns:
        df[column] = pd.to_numeric(df[column], downcast="integer")

    for column in df.select_dtypes(include=["bool"]).columns:
        df[column] = df[column].astype("int8")

    return df


