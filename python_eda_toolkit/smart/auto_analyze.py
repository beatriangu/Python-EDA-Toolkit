def _is_object_like(series: pd.Series) -> bool:
    return (
        pd.api.types.is_object_dtype(series)
        or pd.api.types.is_string_dtype(series)
        or isinstance(series.dtype, pd.CategoricalDtype)
    )


def _detect_datetime_columns(df: pd.DataFrame) -> list[str]:
    datetime_columns: list[str] = []

    date_keywords = (
        "date",
        "time",
        "timestamp",
        "created_at",
        "updated_at",
        "year",
        "month",
        "day",
    )

    date_pattern = (
        r"\d{4}[-/]\d{1,2}[-/]\d{1,2}"
        r"|"
        r"\d{1,2}[-/]\d{1,2}[-/]\d{2,4}"
    )

    for column in df.columns:
        series = df[column]

        if pd.api.types.is_datetime64_any_dtype(series):
            datetime_columns.append(column)
            continue

        if not _is_object_like(series):
            continue

        column_lower = str(column).lower()
        has_date_name = any(keyword in column_lower for keyword in date_keywords)

        sample = series.dropna().astype(str).head(DATETIME_SAMPLE_ROWS)

        if sample.empty:
            continue

        looks_like_date = sample.str.contains(
            date_pattern,
            regex=True,
        ).mean() >= 0.60

        if not has_date_name and not looks_like_date:
            continue

        with warnings.catch_warnings():
            warnings.simplefilter("ignore", UserWarning)
            converted = pd.to_datetime(sample, errors="coerce")

        if converted.notna().mean() >= 0.80:
            datetime_columns.append(column)

    return datetime_columns


def _detect_text_columns(df: pd.DataFrame) -> list[str]:
    text_columns: list[str] = []

    object_columns = df.select_dtypes(
        include=["object", "string"],
    ).columns

    for column in object_columns:
        sample = df[column].dropna().astype(str).head(TEXT_SAMPLE_ROWS)

        if sample.empty:
            continue

        avg_length = sample.str.len().mean()
        unique_ratio = sample.nunique(dropna=True) / max(len(sample), 1)

        if avg_length >= 40 and unique_ratio >= 0.50:
            text_columns.append(column)

    return text_columns


def _detect_id_columns(df: pd.DataFrame) -> list[str]:
    id_columns: list[str] = []
    rows = max(len(df), 1)

    for column in df.columns:
        column_lower = str(column).lower()
        series = df[column]

        explicit_id = (
            column_lower == "id"
            or column_lower.endswith("_id")
            or column_lower in {"uuid", "guid"}
        )

        high_cardinality_text = (
            rows >= 500
            and _is_object_like(series)
            and series.nunique(dropna=True) / rows >= 0.98
        )

        if explicit_id or high_cardinality_text:
            id_columns.append(column)

    return id_columns


def _detect_constant_columns(df: pd.DataFrame) -> list[str]:
    return [
        column
        for column in df.columns
        if df[column].nunique(dropna=False) <= 1
    ]


def _detect_high_cardinality_columns(
    df: pd.DataFrame,
    max_ratio: float = 0.50,
    min_unique: int = 30,
) -> list[str]:
    high_cardinality: list[str] = []

    object_columns = df.select_dtypes(
        include=["object", "string", "category"],
    ).columns

    for column in object_columns:
        non_missing = df[column].dropna()

        if non_missing.empty:
            continue

        unique_count = non_missing.nunique(dropna=True)
        unique_ratio = unique_count / max(len(non_missing), 1)

        if unique_count >= min_unique and unique_ratio >= max_ratio:
            high_cardinality.append(column)

    return high_cardinality


def _detect_column_types(df: pd.DataFrame) -> dict[str, list[str]]:
    # Expensive type detection is done on a bounded sample, while simple dtype
    # lists still use the full dataframe.
    sample = _sample_df(df, PROFILE_SAMPLE_ROWS)

    datetime_columns = _detect_datetime_columns(sample)
    text_columns = _detect_text_columns(sample)
    id_columns = _detect_id_columns(sample)

    numeric_columns = df.select_dtypes(include=["number"]).columns.tolist()
    boolean_columns = df.select_dtypes(include=["bool"]).columns.tolist()

    categorical_columns = [
        column
        for column in df.select_dtypes(
            include=["object", "string", "category", "bool"],
        ).columns.tolist()
        if column not in datetime_columns
        and column not in text_columns
    ]

    return {
        "numeric": numeric_columns,
        "categorical": categorical_columns,
        "datetime": datetime_columns,
        "text": text_columns,
        "boolean": boolean_columns,
        "id": id_columns,
        "constant": _detect_constant_columns(sample),
        "high_cardinality": _detect_high_cardinality_columns(sample),
    }


