def _summarize_missing_values(
    df: pd.DataFrame,
    top_n: int = 10,
) -> list[str]:
    missing = df.isnull().sum()
    missing = missing[missing > 0].sort_values(ascending=False)

    if missing.empty:
        return []

    total_rows = max(len(df), 1)

    return [
        f"{column}: {int(count)} missing "
        f"({round((count / total_rows) * 100, 2)}%)"
        for column, count in missing.head(top_n).items()
    ]


def _summarize_column_types(column_types: dict[str, list[str]]) -> list[str]:
    return [
        f"Numeric columns: {len(column_types['numeric'])}",
        f"Categorical columns: {len(column_types['categorical'])}",
        f"Datetime columns: {len(column_types['datetime'])}",
        f"Text columns: {len(column_types['text'])}",
        f"Boolean columns: {len(column_types['boolean'])}",
        f"Potential ID columns: {len(column_types['id'])}",
        f"Constant columns: {len(column_types['constant'])}",
        f"High-cardinality columns: {len(column_types['high_cardinality'])}",
    ]


def _summarize_classification_target(
    df: pd.DataFrame,
    target: str,
    top_n: int = 20,
) -> list[str]:
    distribution = df[target].value_counts(dropna=False)
    percentages = (
        df[target]
        .value_counts(normalize=True, dropna=False)
        .mul(100)
        .round(2)
    )

    summary = [
        f"{value}: {count} rows ({percentages.loc[value]}%)"
        for value, count in distribution.head(top_n).items()
    ]

    if len(distribution) > top_n:
        summary.append(
            f"... {len(distribution) - top_n} additional classes not shown."
        )

    return summary


def _summarize_regression_target(
    df: pd.DataFrame,
    target: str,
) -> list[str]:
    series = df[target].dropna()

    if series.empty:
        return ["Target contains only missing values."]

    return [
        f"Count: {int(series.count())}",
        f"Mean: {round(series.mean(), 4)}",
        f"Median: {round(series.median(), 4)}",
        f"Standard deviation: {round(series.std(), 4)}",
        f"Minimum: {round(series.min(), 4)}",
        f"Maximum: {round(series.max(), 4)}",
        f"Skewness: {round(series.skew(), 4)}",
        f"Unique values: {series.nunique(dropna=True)}",
    ]


def _summarize_target(
    df: pd.DataFrame,
    target: str | None,
    problem_type: str,
) -> list[str]:
    if not target:
        return []

    if problem_type == "classification":
        return _summarize_classification_target(df, target)

    if problem_type in {"regression", "time_series"} and pd.api.types.is_numeric_dtype(df[target]):
        return _summarize_regression_target(df, target)

    return [
        f"Unique values: {df[target].nunique(dropna=True)}",
        f"Missing target values: {int(df[target].isnull().sum())}",
    ]


